class Producto {
    
    //aqui es donde uno pienza que esto es lo mas sencillo de todo el programa
    
    private int id;
    private String nombre;
    private double precio;
    private int cantidad;

    public Producto(int id, String nombre, double precio, int cantidad) {
        this.id = id;
        this.nombre = nombre;
        this.precio = precio;
        this.cantidad = cantidad;
    }

    public int getId() {
        return id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public double getPrecio() {
        return precio;
    }

    public void setPrecio(double precio) {
        this.precio = precio;
    }

    public int getCantidad() {
        return cantidad;
    }

    public void setCantidad(int cantidad) {
        this.cantidad = cantidad;
    }

    @Override
    public String toString() {
        //el return anterior era mas feo de lo que uno puede pensar
        return "ID: " + id + " \nNombre: " + nombre + "\nPrecio $" + precio + "\nCantidad: " + cantidad + "\n";
    }
}